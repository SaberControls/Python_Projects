while True:


    one = input("Do you want to go left or right? Type L or R:\n")
    one = one.lower()

    if one == "r":
        print("You fell in a hole. Game Over")
    elif one == "l":
        two = input("Do you want to go swim or wait? Type S or W:\n")
        two = two.lower()
        if two == "s":
            print("Attacked by trout. Game Over")
        elif two == "w":
            three = input("Which door? Red, Blue or Yellow? Type R or B or Y:\n")
            three = three.lower()
            if three == "r":
                print("Burned by fire. Game Over")
            elif three == "b":
                print("Eaten by beasts. Game Over")
            elif three == "y":
                print("You win")
                break
            else:
                print("Game Over")
